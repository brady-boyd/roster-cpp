// declare a constant variable for pi
const float pi = 3.14;

// declare a variable for radius and do not assign it a value yet
float radius;

// get the user input and store it in the radius variable
cout << "Enter the radius of the circle: ";
cin >> radius;

// declare a variable for area and assign it the value of pi * radius^2
float area = pi * radius^2;

// print the value of area
cout << "The area of the circle is " << area << endl;